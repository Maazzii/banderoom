package com.project.vo;

public class SpacePicturesVO {

	private int pictureIdx;
	private int spaceIdx;
	private String src;
	
	public int getPictureIdx() {
		return pictureIdx;
	}
	public void setPictureIdx(int pictureIdx) {
		this.pictureIdx = pictureIdx;
	}
	public int getSpaceIdx() {
		return spaceIdx;
	}
	public void setSpaceIdx(int spaceIdx) {
		this.spaceIdx = spaceIdx;
	}
	public String getSrc() {
		return src;
	}
	public void setSrc(String src) {
		this.src = src;
	}
}
